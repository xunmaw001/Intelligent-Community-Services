					this.ro.shifouruzhu = true;
					continue;
					}
					if(o=='ruzhushijian'){
					this.ruleForm.ruzhushijian = obj[o];
					this.ro.ruzhushijian = true;
					continue;
					}
					if(o=='huzhuxingming'){
					this.ruleForm.huzhuxingming = obj[o];
					this.ro.huzhuxingming = true;
					continue;
					}
