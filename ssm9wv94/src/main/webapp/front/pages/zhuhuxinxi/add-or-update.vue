					return
				}
				if((!this.ruleForm.jiatingrenshu)){
					this.$utils.msg(`家庭人数不能为空`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`zhuhuxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`zhuhuxinxi`, this.ruleForm);
				}
