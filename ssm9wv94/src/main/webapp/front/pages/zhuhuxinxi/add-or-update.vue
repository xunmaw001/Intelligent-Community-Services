		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
			// 下二随
			async fangwumingchengChange (e) {
				this.fangwumingchengIndex = e.target.value
				this.ruleForm.fangwumingcheng = this.fangwumingchengOptions[this.fangwumingchengIndex]
				let res = await this.$api.follow(`fangwuxinxi`, `fangwumingcheng`,{
					columnValue: this.ruleForm.fangwumingcheng
				});
				if(res.data.louceng){
					this.ruleForm.louceng = res.data.louceng
				}
				if(res.data.danyuanhao){
					this.ruleForm.danyuanhao = res.data.danyuanhao
				}
			},

			// 多级联动参数

			ruzhushijianChange(e) {
				this.ruleForm.ruzhushijian = e.target.value;
				this.$forceUpdate();
			},


			// 下拉变化
			shifouruzhuChange(e) {
				this.shifouruzhuIndex = e.target.value
				this.ruleForm.shifouruzhu = this.shifouruzhuOptions[this.shifouruzhuIndex]
			},
			// 下拉变化
			xingbieChange(e) {
				this.xingbieIndex = e.target.value
				this.ruleForm.xingbie = this.xingbieOptions[this.xingbieIndex]
			},


