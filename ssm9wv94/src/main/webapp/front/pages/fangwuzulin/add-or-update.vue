<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
				fangwumingcheng: '',
				louceng: '',
				danyuanhao: '',
				fangxing: '',
				tupian: '',
				huxing: '',
				mianji: '',
				jiage: '',
				xiangxidizhi: '',
				ruzhuzhuangtai: '',
				},
				huxingOptions: [],
				huxingIndex: 0,
				ruzhuzhuangtaiOptions: [],
				ruzhuzhuangtaiIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   fangwumingcheng : false,
                                   louceng : false,
                                   danyuanhao : false,
                                   fangxing : false,
                                   tupian : false,
                                   huxing : false,
                                   mianji : false,
                                   jiage : false,
                                   xiangxidizhi : false,
                                   ruzhuzhuangtai : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
			
			
			
			
			
			
			
			
			
			
		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取

			// 自定义下拉框值
			this.huxingOptions = "电梯房,步梯房".split(',')
			// 自定义下拉框值
			this.ruzhuzhuangtaiOptions = "未入住,已入住".split(',')

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`fangwuzulin`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='fangwumingcheng'){
					this.ruleForm.fangwumingcheng = obj[o];
					this.ro.fangwumingcheng = true;
					continue;
					}
					if(o=='louceng'){
					this.ruleForm.louceng = obj[o];
					this.ro.louceng = true;
					continue;
					}
					if(o=='danyuanhao'){
					this.ruleForm.danyuanhao = obj[o];
					this.ro.danyuanhao = true;
					continue;
					}
					if(o=='fangxing'){
					this.ruleForm.fangxing = obj[o];
					this.ro.fangxing = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='huxing'){
					this.ruleForm.huxing = obj[o];
					this.ro.huxing = true;
					continue;
					}
					if(o=='mianji'){
					this.ruleForm.mianji = obj[o];
					this.ro.mianji = true;
					continue;
					}
					if(o=='jiage'){
					this.ruleForm.jiage = obj[o];
					this.ro.jiage = true;
					continue;
					}
					if(o=='xiangxidizhi'){
					this.ruleForm.xiangxidizhi = obj[o];
					this.ro.xiangxidizhi = true;
					continue;
					}
					if(o=='ruzhuzhuangtai'){
					this.ruleForm.ruzhuzhuangtai = obj[o];
					this.ro.ruzhuzhuangtai = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数



			// 下拉变化
			huxingChange(e) {
				this.huxingIndex = e.target.value
				this.ruleForm.huxing = this.huxingOptions[this.huxingIndex]
			},
			// 下拉变化
			ruzhuzhuangtaiChange(e) {
				this.ruzhuzhuangtaiIndex = e.target.value
				this.ruleForm.ruzhuzhuangtai = this.ruzhuzhuangtaiOptions[this.ruzhuzhuangtaiIndex]
			},

			tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				if((!this.ruleForm.fangwumingcheng)){
					this.$utils.msg(`房屋名称不能为空`);
					return
				}
				if((!this.ruleForm.fangxing)){
					this.$utils.msg(`房型不能为空`);
					return
				}
				if((!this.ruleForm.mianji)){
					this.$utils.msg(`面积不能为空`);
					return
				}
				if((!this.ruleForm.jiage)){
					this.$utils.msg(`价格不能为空`);
					return
				}
				if(this.ruleForm.jiage&&(!this.$validate.isIntNumer(this.ruleForm.jiage))){
					this.$utils.msg(`价格应输入整数`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`fangwuzulin`, this.ruleForm);
				}else{
					await this.$api.add(`fangwuzulin`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

